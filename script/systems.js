// Function to load system names from CSV file
// This can be used to dynamically load the data instead of hardcoding
function loadSystemsFromCSV(csvPath) {
  fetch(csvPath)
    .then((response) => response.text())
    .then((data) => {
      // Parse CSV data (one entry per line)
      const loadedNames = data
        .split("\n")
        .map((name) => name.trim())
        .filter((name) => name.length > 0); // Remove empty lines

      // Replace the current array with loaded data
      systemNamesArray.length = 0;
      systemNamesArray.push(...loadedNames);

      console.log(
        `Loaded ${systemNamesArray.length} system names from ${csvPath}`
      );
    })
    .catch((error) => {
      console.error(`Error loading system names from ${csvPath}:`, error);
    });
}

